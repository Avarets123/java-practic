
public class Calculator {
    public int add(int a, int b) {
        return a + b;
    }

    public double multiple(double a, double b) {
        return a * b;
    }

    public int division(int a, int b) {
        return a / b;
    }

    public int substraction(int a, int b) {
        return a - b;
    }

}
